    private void init() {
        try {
            initValidation();
            hideErrors();
            initRepository();
        } catch (Exception ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Severe error", "Cannot initiate form.");
            System.exit(1);
        }
    }

    private void initValidation() {
        validationMap.put(tfUsername, lbUsernameError);
        validationMap.put(tfPassword, lbPasswordError);
    }

    private void hideErrors() {
        validationMap.values().forEach(v -> v.setVisible(false));
    }

    private void initRepository() {
        repository = RepositoryFactory.getRepository();
    }

    private boolean formValid() {
        boolean ok = true;
        for(Map.Entry<JTextComponent, JLabel> entry : validationMap.entrySet()){
            boolean emptyField = entry.getKey().getText().trim().isEmpty();
            ok &= !emptyField;
            entry.getValue().setVisible(emptyField);
        }
        return ok;
    }

    private void showUsernameError() {
        validationMap.get(tfUsername).setVisible(true);
    }

    private void clearForm() {
        validationMap.keySet().forEach(v -> v.setText(""));
        validationMap.values().forEach(v -> v.setVisible(true));
    }
