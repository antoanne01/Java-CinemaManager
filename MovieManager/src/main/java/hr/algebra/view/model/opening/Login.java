    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (!formValid()) {
            return;
        } 
        try {
            String pWord = new String(tfPassword.getPassword());
            Optional<User> optionalUser = repository.loginUser(tfUsername.getText().trim(), pWord);
            
            if(!optionalUser.isPresent()){
                clearForm();
                showUsernameError();
                return;
            }
            
            optionalUser.ifPresent(user -> {
                MovieManager movieManager = new MovieManager(user);
                openingPage.dispose();
            });
            
        } catch (Exception ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Database error", "User do not exists");
            System.exit(1);
        }
    }//GEN-LAST:event_btnLoginActionPerformed


