    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (!formValid()) {
            return;
        }
        try {
            int userExists = repository.checkUserExists(tfUsername.getText().trim());
            if (userExists == 1) {
                MessageUtils.showErrorMessage("Information", "User with that credentials already exists");
                tfUsername.grabFocus();
                tfPassword.setText("");
            } else {
                char[] passWord = tfPassword.getPassword();
                String pwdString = new String(passWord);

                String hashed = BCrypt.hashpw(pwdString, BCrypt.gensalt());
                
                User newUser = new User(tfUsername.getText().trim(), hashed);

                repository.createUser(newUser);
                new MovieManager(newUser);
                openingPage.dispose();

                autoLogin(newUser.getUserName(), newUser.getPassword());
            }

        } catch (Exception ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Severe error", "User with that credentials already exists");
            System.exit(1);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed


