    private void init() {
        try {
            initValidation();
            hideErrors();
            initRepository();
        } catch (Exception ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Database error", "User do not exists");
        }
    }

    private void initValidation() {
        validationMap.put(tfUsername, lbUsernameError);
        validationMap.put(tfPassword, lbPasswordError);
    }

    private void hideErrors() {
        validationMap.values().forEach(e -> e.setVisible(false));
    }

    private void initRepository() {
        try {
            repository = RepositoryFactory.getRepository();
        } catch (Exception ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Database error", "Failed to initialize repository");
        }
    }

    private boolean formValid() {
        boolean ok = true;
        for (Map.Entry<JTextComponent, JLabel> entry : validationMap.entrySet()) {
            boolean emptyField = entry.getKey().getText().trim().isEmpty();
            ok &= !emptyField;
            entry.getValue().setVisible(emptyField);
        }
        return ok;
    }

    private void autoLogin(String username, String password) {
        try {
            Optional<User> optionalUser = repository.loginUser(username, password);
            optionalUser.ifPresent(this::openMovieManager);
        } catch (Exception ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Login Error", "Login failed, try again");
        }
    }

    private void openMovieManager(User user) {
        new MovieManager(user);
        openingPage.dispose();
    }
